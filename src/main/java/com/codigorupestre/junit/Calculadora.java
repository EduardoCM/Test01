package com.codigorupestre.junit;

public class Calculadora {
	
	public long sumar(long valor1, long valor2) {
		return valor1 + valor2;
	}
	
	
	public long restar(long valor1, long valor2){
		return valor1 - valor2;
	}
	
	
	public long multiplicar(long valor1, long valor2){
		return valor1 * valor2;
	}
	
	public long dividir(long valor1, long valor2){
		return valor1 / valor2;
	}

}
